    /**
     * @param args the command line arguments
     */
    public void imprimirCurso(){
        DefaultTableModel modeloTabela = (DefaultTableModel)TabelaCurso.getModel();
        modeloTabela = new DefaultTableModel(new String[] {"C칩digo do Curso", "Nome do Curso", "Carga Hor치ria"}, 0);
        for (int i = 0; i < curso1.getArrayCurso().size(); i++){
            Object[] data = {curso1.getArrayCurso().get(i).getCodigo(), curso1.getArrayCurso().get(i).getNome(), curso1.getArrayCurso().get(i).getCargaHoraria()};
            modeloTabela.addRow(data);
        }
        TabelaCurso.setModel(modeloTabela);
    }
