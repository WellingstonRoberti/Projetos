        curso1.apagar();
        for(int i = 0; i < TabelaCurso.getRowCount();i++){
                Curso curso = new Curso();
                curso.setCodigo(Integer.parseInt(TabelaCurso.getValueAt(i, 0).toString()));
                curso.setCargaHoraria(Integer.parseInt(TabelaCurso.getValueAt(i,2).toString()));
                curso.setNome(TabelaCurso.getValueAt(i,1).toString());
                curso1.setArrayCurso(curso);
        }
        dispose();
